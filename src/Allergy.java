public class Allergy {
    private String name;
    private boolean b;

    public Allergy() {

    }

    public Allergy(String name, boolean b) {
        this.name = name;
        this.b = b;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isB() {
        return b;
    }

    public void setB(boolean b) {
        this.b = b;
    }
}

